# method atau function di python
def cetakNama(nama):
    print("============")
    # Buat list kosong untuk menampung nama
    nama_list = []
    # Tambahkan nama ke dalam list
    nama_list.append(nama)
    # Cetak semua nama dalam list
    for i in nama_list:
        print(i)

cetakNama("nuh")
cetakNama("raihan")
cetakNama("pejuh")
cetakNama("rankuti")
